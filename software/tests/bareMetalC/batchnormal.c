int main() {
    for (int i = 0; i < batch; i ++) {
        for (int j = 0; j < N; j ++) {
            for (int k = 0; k < N; k ++) {
                A[i][j][k] = ((j + k) % 10) * 0.1;
                As[i][j][k] = from_float_to_half(A[i][j][k]);
            }
        }
    }
    printf("Initialization finished!\n");

    long long unsigned start, end;
    
    start = rdcycle();
    void* cgra_din_addr[10] = {As[0], As[1], As[2], As[3], As[4], As[5], As[6], As[7], means, variance};
    void* cgra_dout_addr[10] = {means, variance, Bs[0], Bs[1], Bs[2], Bs[3], Bs[4], Bs[5], Bs[6], Bs[7]}; //{B[0], B[1], B[2], B[3], B[4], B[5], B[6], B[7]};
    mean(cgra_din_addr, cgra_dout_addr);
    variant(cgra_din_addr, cgra_dout_addr);
    fence(0);
    root(cgra_din_addr, cgra_dout_addr);
    volatile int res = fence(1);
    end = rdcycle();
    printf("It takes %d cycles to finish the work via CGRA(%d).\n", end - start, res);
    printf("The mean number is %d.\n", (int)(10000 * from_half_to_float(means[0])));
    printf("The variance is %d.\n", (int)(10000 * from_half_to_float(variance[0])));

    start = rdcycle();
    float mean;
    for (int i = 0; i < batch; i ++) {
        for (int j = 0; j < N; j ++) {
            for (int k = 0; k < N; k ++) {
                mean += A[i][j][k];
            }
        }
    }
    mean = mean / 24200.0;
    
    TYPE root = 0;
    for (int i = 0; i < batch; i ++) {
        for (int j = 0; j < N; j ++) {
            for (int k = 0; k < N; k ++) {
                root += (A[i][j][k] - mean) * (A[i][j][k] - mean);
            }
        }
    }
    root = sqrt(root);

    for (int i = 0; i < batch; i ++) {
        for (int j = 0; j < N; j ++) {
            for (int k = 0; k < N; k ++) {
                R[i][j][k] = (A[i][j][k] - mean) / root;
            }
        }
    }
    end = rdcycle();
    printf("It takes %d to finish the work via CPU.\n", end - start);
    for(int b = 0; b < batch; b ++){
        for(int h = 0; h < N; h = h + 11){
            for(int w = 0; w < N; w = w + 11){
                printf("The number in location (%d, %d, %d): %f(True: %f).\n", b, h, w, from_half_to_float(Bs[b][h][w]), R[b][h][w]);
            }
        }
    }

    return 0;
}