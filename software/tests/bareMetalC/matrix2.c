void cgra_execute(void** din_addr, void** dout_addr, int A_valid, int B_transpose_valid) {
	if (A_valid) {
		load_data(din_addr[0], 0x8000, 4800, 0, 1);
		load_data(din_addr[1], 0x0, 4800, 0, 1);
		load_data(din_addr[2], 0xc000, 4800, 0, 1);
		load_data(din_addr[3], 0x2000, 4800, 0, 1);
		load_data(din_addr[4], 0x18000, 4800, 0, 1);
		load_data(din_addr[5], 0x1a000, 4800, 0, 1);
	}
	else {
		reload_data(0x8000, 1);
		reload_data(0x0, 1);
		reload_data(0xc000, 1);
		reload_data(0x2000, 1);
		reload_data(0x18000, 1);
		reload_data(0x1a000, 1);
	}
	if (B_transpose_valid) {
		load_data(din_addr[6], 0xa000, 4800, 0, 1);
	}
	else {
		reload_data(0xa000, 1);
	}
	execute(0xf8ff, 1);
	store(dout_addr[0], 0xe000, 40, 1);
	store(dout_addr[1], 0x4000, 40, 1);
	store(dout_addr[2], 0x1c000, 40, 1);
	store(dout_addr[3], 0x6000, 40, 1);
	store(dout_addr[4], 0x1e000, 40, 1);
	store(dout_addr[5], 0x10000, 40, 1);
}

