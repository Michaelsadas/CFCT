#define TYPE float
#define batch 8
#define N 55

TYPE A[batch][N + 1][N];
TYPE B[batch][N + 1][N];
TYPE R[batch][N + 1][N];
TYPE Mean[2] = {4.49586, 1.0};
TYPE res[2];
TYPE root[2] = {7989.1, 89.381};

void init() {
    for (int i = 0; i < batch; i ++) {
        for (int j = 0; j < N; j ++) {
            for (int k = 0; k < N; k ++) {
                A[i][j][k] = (j + k) % 10;
            }
        }
    }
}

void batchnormalization() {
    TYPE result = 0;
    for (int i = 0; i < batch; i ++) {
        for (int j = 0; j < N; j ++) {
            for (int k = 0; k < N; k ++) {
                R[i][j][k] = (A[i][j][k] - Mean[0]) / root[1];
            }
        }
    }
}

