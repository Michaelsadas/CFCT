void init(){
    for(int j = 0; j < NJ; j++) {
        for (int i = 0; i < NI; i++){
            A[j][i] = (TYPE)((i + j) % NJ); 
        }
    }
}

void conv2() {
	for (int j = 0; j < NJ-2; j++) {
    	for (int i = 0; i < NI - 2; i++){
	    /*For 3x3 filter*/
		    B[j][i] =   c11 * A[j][i]     + c12 * A[j][i + 1]      + c13 * A[j][i + 2]
				      + c21 * A[j + 1][i] + c22 * A[j + 1][i + 1]  + c23 * A[j + 1][i + 2]
				      + c31 * A[j + 2][i] + c32 * A[j + 2][i + 1]  + c33 * A[j + 2][i + 2];

      	}
    }
}

void result_check(){
    for (int j = 0; j < NJ; j++){
        for(int i = 0; i < NI; i++) {
            if(C[j][i] != B[j][i]) 
			printf("WRONG IN LOCATION [%d, %d]: %d, %d\n", j, i, (int)C[j][i], (int)B[j][i]);
        }
    }
}

