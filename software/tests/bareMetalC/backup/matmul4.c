void cgra_execute(void** din_addr, void** dout_addr, int A_valid, int B_transpose_valid, int task_id) {
	if (A_valid) {
		load_data(din_addr[0], 0x8000, 120, task_id);
		load_data(din_addr[1], 0xc000, 120, task_id);
		load_data(din_addr[2], 0x0, 120, task_id);
		load_data(din_addr[3], 0x18000, 120, task_id);
		load_data(din_addr[4], 0x1a000, 120, task_id);
		load_data(din_addr[5], 0x10000, 120, task_id);
	}
	else {
		reload_data(0x8000, task_id);
		reload_data(0xc000, task_id);
		reload_data(0x0, task_id);
		reload_data(0x18000, task_id);
		reload_data(0x1a000, task_id);
		reload_data(0x10000, task_id);
	}
	if (B_transpose_valid) {
		load_data(din_addr[6], 0xa000, 720, task_id);
	}
	else {
		reload_data(0xa000, task_id);
	}
	execute(0xfcfe, task_id);
	store(dout_addr[0], 0xe000, 24, task_id);
	store(dout_addr[1], 0x2000, 24, task_id);
	store(dout_addr[2], 0x4000, 24, task_id);
	store(dout_addr[3], 0x1c000, 24, task_id);
	store(dout_addr[4], 0x1e000, 24, task_id);
	store(dout_addr[5], 0x12000, 24, task_id);
}

void result_check()
{
    for(int j = 0; j < M; j++){
		for(int i = 0; i < M; i++){
        	if(C[i][j] != D[i][j]) printf("There is an error in [%d, %d]: [%d, %d]\n", i, j, C[i][j], D[i][j]);
		}
    }
    printf("done.\n");
}

