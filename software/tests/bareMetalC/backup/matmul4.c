#define TYPE int
#define N 120
#define M 120
#define S 6

TYPE A[M][N];
TYPE B_transpose[M][N];
TYPE C[M][M];
TYPE D[M][M];

static
void init_array()
{
    int i, j;
    for (i = 0; i < N; i++)
    {
        for (j = 0; j < M; j ++) {
			A[j][i] = (TYPE)((i + j) % N);
            B_transpose[j][i] = (TYPE) ((i + j) % N);
        }
    }
}

static
void matmul()
{
    int i, j, k;
	int temp;
    for (i = 0; i < M; i++) {
        for (j = 0; j < M; j++) {
			temp = 0;
            for (k = 0; k < N; k++){
            	temp += A[i][k] * B_transpose[j][k];
			}
			D[i][j] = temp;
        }
    }
}

