	load_data(din_addr[1], 0x2000, 3840, 1, 1);
	load_data(din_addr[2], 0x4000, 3840, 1, 1);
	load_data(din_addr[3], 0x6000, 3840, 1, 1);
	load_data(din_addr[4], 0x10000, 3840, 0, 1);
	load_data(din_addr[5], 0x18000, 3840, 1, 1);
	load_data(din_addr[6], 0x1a000, 3840, 1, 1);
	load_data(din_addr[7], 0x8000, 3840, 1, 1);
