#define TYPE float
#define batch 8
#define N 55

TYPE A[batch][N + 1][N];
TYPE Mean[2] = {4.49587, 1.0};
TYPE res[2];

void init() {
    for (int i = 0; i < batch; i ++) {
        for (int j = 0; j < N; j ++) {
            for (int k = 0; k < N; k ++) {
                A[i][j][k] = (j + k) % 10;
            }
        }
    }
}

TYPE MS() {
    TYPE result = 0;
    for (int i = 0; i < batch; i ++) {
        for (int j = 0; j < N; j ++) {
            for (int k = 0; k < N; k ++) {
                result += (A[i][j][k] - Mean[0]) * (A[i][j][k] - Mean[0]);
            }
        }
    }
    return result / 3025.0;
}

