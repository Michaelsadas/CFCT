#include <stdio.h>
#include "include/encoding.h"
#include "include/ISA.h"
#define TYPE float
#define batch 8
#define N 55

TYPE A[batch][N + 1][N];
TYPE Mean[2];

void init() {
    for (int i = 0; i < batch; i ++) {
        for (int j = 0; j < N; j ++) {
            for (int k = 0; k < N; k ++) {
                A[i][j][k] = (j + k) % 10;
            }
        }
    }
}

TYPE mean() {
    TYPE result = 0;
    for (int i = 0; i < batch; i ++) {
        for (int j = 0; j < N; j ++) {
            for (int k = 0; k < N; k ++) {
                result += A[i][j][k];
            }
        }
    }
    return result / 24200.0;
}

